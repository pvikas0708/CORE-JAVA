package in.array;

public class Boolean_Array {

	public static void main(String[] args) {
		boolean[] arr = new boolean[3];

		arr[0] = true;
		arr[1] = false;
		arr[2] = true;

		for (int i = 0; i < arr.length; i++) {
			System.out.println(arr[i]);
		}

	}
}
//Default value of Boolean Array=> false